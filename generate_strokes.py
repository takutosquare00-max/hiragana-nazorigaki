# ひらがな→romaji マッピング（index.html の HIRAGANA 順）
CHAR_TO_ROM = {
    'あ': 'a', 'い': 'i', 'う': 'u', 'え': 'e', 'お': 'o',
    'か': 'ka', 'き': 'ki', 'く': 'ku', 'け': 'ke', 'こ': 'ko',
    'さ': 'sa', 'し': 'si', 'す': 'su', 'せ': 'se', 'そ': 'so',
    'た': 'ta', 'ち': 'ti', 'つ': 'tu', 'て': 'te', 'と': 'to',
    'な': 'na', 'に': 'ni', 'ぬ': 'nu', 'ね': 'ne', 'の': 'no',
    'は': 'ha', 'ひ': 'hi', 'ふ': 'hu', 'へ': 'he', 'ほ': 'ho',
    'ま': 'ma', 'み': 'mi', 'む': 'mu', 'め': 'me', 'も': 'mo',
    'や': 'ya', 'ゆ': 'yu', 'よ': 'yo',
    'ら': 'ra', 'り': 'ri', 'る': 'ru', 'れ': 're', 'ろ': 'ro',
    'わ': 'wa', 'を': 'wo', 'ん': 'n',
}

# si->shi, ti->chi, tu->tsu のマッピング
ROM_ALIAS = {'si': 'shi', 'ti': 'chi', 'tu': 'tsu'}

VB = 109
SCALE = VB / 100.0


def coords_to_path_d(coords):
    """座標列を SVG path d に変換（109x109 viewBox に統一スケール）
    KanaDiagrams の 100x100 基準を維持"""
    if not coords:
        return ''
    pts = [(round(x * SCALE, 2), round(y * SCALE, 2)) for x, y in coords]
    if len(pts) == 1:
        return f'M {pts[0][0]} {pts[0][1]}'
    if len(pts) == 2:
        return f'M {pts[0][0]} {pts[0][1]} L {pts[1][0]} {pts[1][1]}'
    parts = [f'M {pts[0][0]} {pts[0][1]}']
    i = 1
    while i + 2 < len(pts):
        (x1, y1), (x2, y2), (x3, y3) = pts[i], pts[i + 1], pts[i + 2]
        parts.append(f'C {x1} {y1} {x2} {y2} {x3} {y3}')
        i += 3
    while i < len(pts):
        parts.append(f'L {pts[i][0]} {pts[i][1]}')
        i += 1
    return ' '.join(parts)


def main():
    lines = [
        '// ひらがなストロークデータ（hiragana_hitsuzyun2016.pdf 準拠）',
        '// 元データ: KanaDiagrams (MIT) https://github.com/SestrenExsis/KanaDiagrams',
        '// 文部科学省「ひらがなの筆順」に準拠',
        '',
        'const HIRAGANA_STROKES = {'
    ]
    for ch, rom in CHAR_TO_ROM.items():
        key = ROM_ALIAS.get(rom, rom)
        if key not in diagrams:
            print(f'Warning: {ch} ({rom}) not in diagrams', file=__import__('sys').stderr)
            continue
        strokes = diagrams[key]
        paths = [coords_to_path_d(s) for s in strokes]
        paths_js = '[' + ', '.join(repr(p) for p in paths) + ']'
        lines.append(f"  '{ch}': {paths_js},")
    lines.append('};')
    lines.append('')
    lines.append('if (typeof module !== "undefined" && module.exports) module.exports = HIRAGANA_STROKES;')
    return '\n'.join(lines)


if __name__ == '__main__':
    print(main())
